// Lấy danh sách sản phẩm theo danh mục
export const getProductsByCategory = (categorySlug) => {
  if (!categorySlug) return products;
  return products.filter(product => product.category === categorySlug || product.categories.includes(categorySlug));
};

// Lấy sản phẩm theo slug
export const getProductBySlug = (slug) => {
  return products.find(product => product.slug === slug);
};

// Lấy danh sách sản phẩm liên quan (cùng danh mục, không bao gồm sản phẩm hiện tại)
export const getRelatedProducts = (currentProductId, categorySlug, limit = 4) => {
  return products
    .filter(product => (product.category === categorySlug || product.categories.includes(categorySlug)) && product.id !== currentProductId)
    .slice(0, limit);
};